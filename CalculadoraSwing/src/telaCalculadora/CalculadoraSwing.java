    private void btnClickActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClickActionPerformed
        // TODO add your handling code here:
        int num = Integer.parseInt(txtNumero.getValue().toString());
        double raiz = Math.sqrt(num); 
        float rest = num % 2;
        float cubo = (float) Math.pow(num ,3 );
        float cubica = (float) Math.cbrt(num);
        int abs = Math.abs(num);
        lblResto.setText(Float.toString(rest));
        lblCubo.setText(Float.toString(cubo));
        lblRaiz.setText(Double.toString(raiz));
        lblCubica.setText(Float.toString(cubica));
        lblAbsoluto.setText(Integer.toString(abs));
    }//GEN-LAST:event_btnClickActionPerformed

